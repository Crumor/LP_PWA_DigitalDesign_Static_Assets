

/*PRINCIPAL*/

$('.principal-lb').mouseenter(function()
{
    $('.principal-lb-circulo h5').css("font-size", "30px");
    $('.principal-lb-circulo p').css("font-size", "25px").css("opacity", "0").css("padding-top", "20%");
    $('.linea-lb').css("opacity", "0");
});


$('.principal-lb').mouseleave(function()
{
    $('.principal-lb-circulo h5').css("font-size", "25px");
    $('.principal-lb-circulo p').css("font-size", "30px").css("opacity", "1").css("padding-top", "30%");
    $('.linea-lb').css("opacity", "1");

});



